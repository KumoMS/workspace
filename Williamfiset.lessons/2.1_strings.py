# 2.1 strings

# we have single and double quotations for strings

single_quote_str = 'single quotes'
double_quote_str = "double quotes"

# Here we can use the to different quotes within this string
sentence1 = 'I "really" like chocolate'
sentence2 = "I 'really' like chocolate"
print(sentence1)
print(sentence2)

# the use of a backslash allows use to use the same type of quotes

sentence3 = "I \"really\" like chocolate"
print(sentence3)

# triple quotes can be used to write paragraphs and can also be used for comments like the # does

paragraph = '''This is
paragraph
using triple quotes'''
print(paragraph)